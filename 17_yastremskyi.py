# Задати масив 34 цілих чисел. Вивести масив на екран.
# Виділити зірочкою всі елементи масиву, які мають парні значення.
# Задати ціле число p. Порахувати кількість всіх парних значень масиву більших за p.

# ANSI-коди для виділення кольором у терміналі
RED = "\033[31m"   # червоний — для парних елементів
RESET = "\033[0m"


def main():
    # Масив задано в коді програми (без використання генератора випадкових чисел).
    # Узято такий самий масив, як у прикладі завдання, для перевірки результату.
    array = [
        5,  3, -6,  5,  0,  3,  1, -4,  1,  3,
        9,  0,  6,  1,  8,  4,  4, -1,  1, 12,
       10,  9,  1,  7,  7, 11, 12, 16, 23,  9,
        7,  3,  4,  1
    ]

    # 1) Вивід масиву на екран із виділенням парних елементів зірочкою.
    print(f"Масив {len(array)} елементів:")
    parts = []
    for x in array:
        if x % 2 == 0:                       # парне (включно з 0 та від'ємними)
            parts.append(f"{RED}{x}*{RESET}")
        else:
            parts.append(str(x))
    print(" ".join(parts))

    # 2) Введення цілого числа p з клавіатури з обробкою некоректного вводу.
    while True:
        raw = input("Введіть ціле число p: ").strip()
        try:
            p = int(raw)
            break
        except ValueError:
            print("Помилка: потрібно ввести ціле число. Спробуйте ще раз.")

    # 3) Підрахунок кількості парних елементів, які більші за p.
    count = sum(1 for x in array if x % 2 == 0 and x > p)

    print(f"Кількість парних елементів більша {p}: {count}")


if __name__ == "__main__":
    main()
